
// Variables en asiganción
var producto = 'Monitor de 24 pulgadas';
console.log(producto);

// Las variables se pueden reasignar
producto = 'Monitor de 19 pulgadas';
console.log(producto);

// Javascript es un lenguaje de tipado dinámico, no se especifica el tipo de dato de la variable
producto = 20;
console.log(producto);

// También se pueden inicializar las variables sin valor y asignarlo después
