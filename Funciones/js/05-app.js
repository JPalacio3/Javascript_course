// PARÁMETROS Y ARGUMENTOS DE FUNCIONES: Los parámetros son variables que se asignan al momento de definir la función, y los argumentos son los valores que se declaran al momento de invocar dicha función

function sumar(a,b) // Los parámetros a y b son variables que se pasan al declarar la variable
{
    console.log(a + b);
}
sumar(4,5); // Los argumentos son los valores que se asignan a dichas variables y se pasan al invocar la función
sumar(2515,64565);
sumar(234,344);

function saludar(nombre,apellido)
{
    console.log(`Hola ${nombre} ${apellido}`);
}
saludar('Joel','Palacio');
saludar('Alberto','Cano');
