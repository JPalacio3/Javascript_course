// ARROW FUNCTIONS O FUNCIONES FLECHA : Las funciones flecha (=>) en JavaScript son una sintaxis concisa para escribir funciones y mantienen un this léxico, lo que puede ser útil en ciertos contextos.

// Función declarada de la manera tradicional
const aprendiendo = function () {
    console.log('Aprendiendo Javascript');
}
aprendiendo();

// Arrow function
const aprendiendo2 = () => 'Aprendiendo Javascript 2';

console.log(aprendiendo2());
