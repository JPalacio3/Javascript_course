// FUNCIONES : Las funciones en JavaScript son bloques de código reutilizable que realizan tareas específicas o calculan valores. Pueden tomar argumentos como entrada y devolver resultados, lo que permite encapsular la lógica de un programa para su reutilización y organización.


// Declaración de Función ( Function Declaration )
function sumar()
{
    console.log(2 + 2);
}
sumar();
sumar(); // Las funciones son reutilizables
sumar();

// Expresión de Función ( Function Expression )
const sumar2 = function ()
{
    console.log(3 + 3);
}
sumar2();
